public class VIChild {
    private String name;
    private char gender;
    private int age;
    private int score;
    private int hint;
    private int stage;
    private int level;

    public VIChild() {
    }

    public VIChild(String name, char gender, int age, int score, int hint, int stage, int level) {
        this.name = name;
        this.gender = gender;
        this.age = age;
        this.score = score;
        this.hint = hint;
        this.stage = stage;
        this.level = level;
    }
    
    
}
